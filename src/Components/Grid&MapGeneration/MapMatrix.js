export function generateMapMatrix() {
  const numberOfRow = 27;
  const numberOfColumn = 27;

  const numberOfUnits = numberOfRow * numberOfColumn;

  //middle column = 14

  let mapArray = [];

  for (let j = 0; j <= numberOfColumn; j++) {
    for (let i = 0; i <= Math.floor(numberOfColumn / 2); i++) {
      mapArray[
        Math.floor(numberOfUnits / 2) + Math.floor(numberOfColumn / 2) * j + i
      ] = 0;
      mapArray[
        Math.floor(numberOfUnits / 2) - Math.floor(numberOfColumn / 2) * j - i
      ] = 1;
    }
  }

  console.log(Math.round(numberOfUnits / 2));

  //mapArray[Math.floor(numberOfUnits / 2)] = 0;

  return mapArray;
}
